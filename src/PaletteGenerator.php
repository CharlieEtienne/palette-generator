    public static function rgbToOklch(array $rgb): array
    {
        [$red, $green, $blue] = $rgb;

        // Normalize RGB values to between 0 and 1
        $red /= 255;
        $green /= 255;
        $blue /= 255;

        // Linearize RGB values
        [$red, $green, $blue] = self::convertToLinearSpace($red, $green, $blue);

        // Convert to linear light values
        $long = 0.4122214708 * $red + 0.5363325363 * $green + 0.0514459929 * $blue;
        $medium = 0.2119034982 * $red + 0.6806995451 * $green + 0.1073969566 * $blue;
        $short = 0.0883024619 * $red + 0.2817188376 * $green + 0.6299787005 * $blue;

        // Apply the OKLab transformation
        $longCubeRoot = pow($long, 1 / 3);
        $mediumCubeRoot = pow($medium, 1 / 3);
        $shortCubeRoot = pow($short, 1 / 3);

        $lightness = 0.2104542553 * $longCubeRoot + 0.793617785 * $mediumCubeRoot - 0.0040720468 * $shortCubeRoot;

        $colorOpponentA = 1.9779984951 * $longCubeRoot - 2.428592205 * $mediumCubeRoot + 0.4505937099 * $shortCubeRoot;
        $colorOpponentB = 0.0259040371 * $longCubeRoot + 0.7827717662 * $mediumCubeRoot - 0.808675766 * $shortCubeRoot;

        $chroma = sqrt($colorOpponentA * $colorOpponentA + $colorOpponentB * $colorOpponentB);
        $hue = atan2($colorOpponentB, $colorOpponentA); // In radians

        // Convert hue to degrees and ensure it's between 0 and 360
        $hue = rad2deg($hue);

        if ($hue < 0) {
            $hue += 360;
        }

        return [round($lightness, 3), round($chroma, 3), round($hue, 3)];
    }

    public static function hexToRGBArray($color): array
    {
        return sscanf($color, '#%02x%02x%02x');
    }

    public static function xyzToLab ($xyz): array
    {
        [$x, $y, $z] = $xyz;

        $x /= 95.047;
        $y /= 100;
        $z /= 108.883;

        $x = $x > 0.008856 ? pow($x, 1 / 3) : $x * 7.787 + 16 / 116;
        $y = $y > 0.008856 ? pow($y, 1 / 3) : $y * 7.787 + 16 / 116;
        $z = $z > 0.008856 ? pow($z, 1 / 3) : $z * 7.787 + 16 / 116;

        $l = $y * 116 - 16;
        $a = ($x - $y) * 500;
        $b = ($y - $z) * 200;

        return [ $l, $a, $b ];
    }

    public static function rgbToXyz($rgb): array
    {
        [$r, $g, $b] = $rgb;

        [$r, $g, $b] = self::convertToLinearSpace($r, $g, $b);

        $r *= 100;
        $g *= 100;
        $b *= 100;

        $x = $r * 0.412453 + $g * 0.357580 + $b * 0.180423;
        $y = $r * 0.212671 + $g * 0.715160 + $b * 0.072169;
        $z = $r * 0.019334 + $g * 0.119193 + $b * 0.950227;

        return [ $x, $y, $z];
    }

    public static function convertToLinearSpace(mixed $r, mixed $g, mixed $b): array
    {
        $r = $r <= 0.04045 ? $r / 12.92 : pow(($r + 0.055) / 1.055, 2.4);
        $g = $g <= 0.04045 ? $g / 12.92 : pow(($g + 0.055) / 1.055, 2.4);
        $b = $b <= 0.04045 ? $b / 12.92 : pow(($b + 0.055) / 1.055, 2.4);

        return [$r, $g, $b];
    }

    public static function delta2000(array $lab1, array $lab2): float
    {
        [$l1, $a1, $b1] = $lab1;
        [$l2, $a2, $b2] = $lab2;

        $avg_lp     = ($l1 + $l2) / 2;
        $c1         = sqrt(pow($a1, 2) + pow($b1, 2));
        $c2         = sqrt(pow($a2, 2) + pow($b2, 2));
        $avg_c      = ($c1 + $c2) / 2;
        $g          = (1 - sqrt(pow($avg_c , 7) / (pow($avg_c, 7) + pow(25, 7)))) / 2;
        $a1p        = $a1 * (1 + $g);
        $a2p        = $a2 * (1 + $g);
        $c1p        = sqrt(pow($a1p, 2) + pow($b1, 2));
        $c2p        = sqrt(pow($a2p, 2) + pow($b2, 2));
        $avg_cp     = ($c1p + $c2p) / 2;
        $h1p        = rad2deg(atan2($b1, $a1p));
        if ($h1p < 0) {
            $h1p    += 360;
        }
        $h2p        = rad2deg(atan2($b2, $a2p));
        if ($h2p < 0) {
            $h2p    += 360;
        }
        $avg_hp     = abs($h1p - $h2p) > 180 ? ($h1p + $h2p + 360) / 2 : ($h1p + $h2p) / 2;
        $t          = 1 - 0.17 * cos(deg2rad($avg_hp - 30)) + 0.24 * cos(deg2rad(2 * $avg_hp)) + 0.32 * cos(deg2rad(3 * $avg_hp + 6)) - 0.2 * cos(deg2rad(4 * $avg_hp - 63));
        $delta_hp   = $h2p - $h1p;
        if (abs($delta_hp) > 180) {
            if ($h2p <= $h1p) {
                $delta_hp += 360;
            }
            else {
                $delta_hp -= 360;
            }
        }
        $delta_lp   = $l2 - $l1;
        $delta_cp   = $c2p - $c1p;
        $delta_hp   = 2 * sqrt($c1p * $c2p) * sin(deg2rad($delta_hp) / 2);

        $s_l        = 1 + ((0.015 * pow($avg_lp - 50, 2)) / sqrt(20 + pow($avg_lp - 50, 2)));
        $s_c        = 1 + 0.045 * $avg_cp;
        $s_h        = 1 + 0.015 * $avg_cp * $t;

        $delta_ro   = 30 * exp(-(pow(($avg_hp - 275) / 25, 2)));
        $r_c        = 2 * sqrt(pow($avg_cp, 7) / (pow($avg_cp, 7) + pow(25, 7)));
        $r_t        = -$r_c * sin(2 * deg2rad($delta_ro));

        $kl = $kc = $kh = 1;

        return sqrt(pow($delta_lp / ($s_l * $kl), 2) + pow($delta_cp / ($s_c * $kc), 2) + pow($delta_hp / ($s_h * $kh), 2) + $r_t * ($delta_cp / ($s_c * $kc)) * ($delta_hp / ($s_h * $kh)));
    }

    public static function nearestTailwindColor(string $color): array
    {
        $targetHex = $color;
        $targetRgb = self::hexToRGBArray($targetHex);
        $targetXyz = self::rgbToXyz($targetRgb);
        $targetLab = self::xyzToLab($targetXyz);
        $targetOklch = self::rgbToOklch($targetRgb);

        $target = compact('targetHex', 'targetRgb', 'targetXyz', 'targetLab', 'targetOklch');

        $colors = self::tailwindColors();
        $l = $targetLab[0];
        $lBucket = floor($l / 1000) * 1000;

        // Search in the target partition and adjacent partitions
        $searchPartitions = [
            $lBucket - 1000 => $colors['partitions'][$lBucket - 1000] ?? [],
            $lBucket => $colors['partitions'][$lBucket] ?? [],
            $lBucket + 1000 => $colors['partitions'][$lBucket + 1000] ?? []
        ];

        $closest = null;
        $bestScore = INF;

        foreach ($searchPartitions as $partition) {
            foreach ($partition as $tColor) {
                $tailwindLab = $tColor['lab'];
                $dE = self::delta2000($tailwindLab, $targetLab);
                if ($dE < $bestScore) {
                    $bestScore = $dE;
                    $closest = $tColor;
                }
            }
        }


        $paletteForClosest = array_filter($colors['data'], fn($tColor) => $tColor['color'] === $closest['color']);

        return compact('closest', 'paletteForClosest', 'target');
    }

    public static function generateShiftedTailwindOklchPalette(array $data): array
    {
        $closest = $data['closest']['oklch'];
        $target = $data['target']['targetOklch'];
        $palette = $data['paletteForClosest'];

        $diff = $target[2] - $closest[2];
        if ($diff > 180) {
            $diff -= 360;
        } elseif ($diff < -180) {
            $diff += 360;
        }
        $hueDifference = $diff;

        $delta = [
            $target[0] - $closest[0], // ΔL
            $target[1] - $closest[1], // ΔC
            $hueDifference,
        ];

        $newPalette = [];

        foreach ($palette as $color) {
            $shade = (int) $color['shade'];
            [$l, $c, $h] = $color['oklch'];

            // Progressive smoothing to the extremes
            $factor = 1 - pow(abs($shade - 500) / 450, 2);

            $newL = max(0, min(1, $l + $delta[0] * $factor));
            $newC = max(0, $c + $delta[1] * $factor);
            $h = fmod($h + $delta[2], 360);
            $newH = $h < 0 ? $h + 360 : $h;

            $newPalette[$shade] = sprintf(
                'oklch(%s %s %s)',
                round($newL, 3),
                round($newC, 3),
                round($newH, 3)
            );
        }

        ksort($newPalette);
        return $newPalette;
    }

    public static function generatePalette(string $color): array
    {
        $data = self::nearestTailwindColor($color);
        return self::generateShiftedTailwindOklchPalette($data);
    }

    private static function createPartitions(array $colors): array
    {
        // Create partitions based on L value ranges (for example)
        $partitions = [];

        foreach ($colors as $color) {
            $l = $color['lab'][0];
            $lBucket = floor($l / 1000) * 1000; // Group by thousands

            if (!isset($partitions[$lBucket])) {
                $partitions[$lBucket] = [];
            }

            $partitions[$lBucket][] = $color;
        }

        return $partitions;
    }

